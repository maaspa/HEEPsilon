void config()
{
	for(int i = 0; i < 40; i++ )
	{
		i_testVect_soft[i] = kcom_getRand() % (UINT_MAX - 1 - 0 + 1) + 0;
		i_testVect_cgra[i] = i_testVect_soft[i];
	}

	cgra_input[0][0] = (uint32_t)&cgra_input[0][0];
    cgra_input[3][2] = (uint32_t)&cgra_output[3][2];
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            cgra_input[i][j] = (uint32_t)i_testVect_cgra;
        }
    }

    cgra_input[0][0] = (uint32_t)&cgra_input[0][0];
    cgra_input[3][2] = (uint32_t)&cgra_output[3][2];
}

